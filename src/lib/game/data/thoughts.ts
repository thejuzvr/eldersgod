export function getRandomThought(category: ThoughtCategory, heroName: string): string {
  const categoryThoughts = thoughts[category];
  const thought = categoryThoughts[Math.floor(Math.random() * categoryThoughts.length)];
  // Replace {hero} placeholder with actual hero name
  return thought.replace(/{hero}/gi, heroName);
}
