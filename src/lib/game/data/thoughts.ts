export function getRandomThought(category?: keyof typeof thoughts): string {
	if (category && thoughts[category]) {
		const categoryThoughts = thoughts[category];
		return categoryThoughts[Math.floor(Math.random() * categoryThoughts.length)];
	}
	
	return allThoughts[Math.floor(Math.random() * allThoughts.length)];
}

export function getIdleThought(): string {
	return getRandomThought('idle');
}

