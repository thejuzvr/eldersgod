export function getRandomAction(category?: keyof typeof actions): string {
	if (category && actions[category]) {
		const categoryActions = actions[category];
		return categoryActions[Math.floor(Math.random() * categoryActions.length)];
	}
	
	// Случайная категория
	const categories = Object.keys(actions) as (keyof typeof actions)[];
	const randomCategory = categories[Math.floor(Math.random() * categories.length)];
	const categoryActions = actions[randomCategory];
	return categoryActions[Math.floor(Math.random() * categoryActions.length)];
}

