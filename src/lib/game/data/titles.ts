export function getRandomTitle(): string {
	return titles[Math.floor(Math.random() * titles.length)];
}

export function getTitleByLevel(level: number): string {
	if (level < 5) {
		return getRandomTitle(); // Начальные титулы
	} else if (level < 10) {
		const heroicTitles = titles.filter(t => 
			t.includes('Покоритель') || t.includes('Укротитель') || t.includes('Друг')
		);
		return heroicTitles[Math.floor(Math.random() * heroicTitles.length)] || getRandomTitle();
	} else if (level < 20) {
		const nobleTitles = titles.filter(t =>
			t.includes('Лорд') || t.includes('Барон') || t.includes('Граф') || t.includes('Мастер')
		);
		return nobleTitles[Math.floor(Math.random() * nobleTitles.length)] || getRandomTitle();
	} else {
		const royalTitles = titles.filter(t =>
			t.includes('Король') || t.includes('Царь') || t.includes('Император')
		);
		return royalTitles[Math.floor(Math.random() * royalTitles.length)] || getRandomTitle();
	}
}

